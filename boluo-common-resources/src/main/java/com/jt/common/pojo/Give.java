package com.jt.common.pojo;

public class Give {
    //送养信息表

    private Integer giveId;     //送养信息表ID
    private String giveTitle;   //标题
    private String giveImage;   //图片
    private String giveInfo;    //信息

    public Integer getGiveId() {
        return giveId;
    }

    public void setGiveId(Integer giveId) {
        this.giveId = giveId;
    }

    public String getGiveTitle() {
        return giveTitle;
    }

    public void setGiveTitle(String giveTitle) {
        this.giveTitle = giveTitle;
    }

    public String getGiveImage() {
        return giveImage;
    }

    public void setGiveImage(String giveImage) {
        this.giveImage = giveImage;
    }

    public String getGiveInfo() {
        return giveInfo;
    }

    public void setGiveInfo(String giveInfo) {
        this.giveInfo = giveInfo;
    }

}
